// Ⅱ Dart 연산자
// 01. 산술 연산자
void main() {
  // 더하기
  print('3 + 2 = ${3 + 2}'); // 더하기

  // 빼기
  print('3 - 2 = ${3 - 2}'); // 빼기

  // 곱하기
  print('3 * 2 = ${3 * 2}'); // 곱하기

  // 나누기
  print('3 / 2 = ${3 / 2}'); // 나누기

  // 나머지
  print('3 % 2 = ${3 % 2}'); // 나머지

  // 몫
  print('5 ~/ 2 = ${5 ~/ 2}'); // 몫
}
